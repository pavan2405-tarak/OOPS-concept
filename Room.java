package Pratice;

public class Room {
	private TV tv;
	private Chair chair;
	private Fan fan;
	private Doors doors;
	public TV getTv() {
		return tv;
	}
	public void setTv(TV tv) {
		this.tv = tv;
	}
	public Chair getChair() {
		return chair;
	}
	public void setChair(Chair chair) {
		this.chair = chair;
	}
	public Fan getFan() {
		return fan;
	}
	public void setFan(Fan fan) {
		this.fan = fan;
	}
	public Doors getDoors() {
		return doors;
	}
	public void setDoors(Doors doors) {
		this.doors = doors;
	}
	 
	
}
