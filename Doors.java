package Pratice;

public class Doors extends Room {
	private long Size;
	private String type;
	private String Design;
	private long length;
	public long getSize() {
		return Size;
	}
	public void setSize(long size) {
		Size = size;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getDesign() {
		return Design;
	}
	public void setDesign(String design) {
		Design = design;
	}
	public long getLength() {
		return length;
	}
	public void setLength(long length) {
		this.length = length;
	}
	

}
